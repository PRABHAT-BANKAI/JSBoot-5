let basket = "apple";

switch (basket) {
  case "apple":
    console.log("The basket contains apples");

    break;
  case "guava":
    console.log("The basket contains guava");
    break;

  case "banana":
    console.log("The basket contains bananas");
    break;

  case "orange":
    console.log("The basket contains oranges");
    break;

  default:
    console.log("The basket is empty");
    break;
}
